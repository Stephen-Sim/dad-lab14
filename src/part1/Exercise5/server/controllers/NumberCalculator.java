package part1.Exercise5.server.controllers;

public class NumberCalculator {

	/**
	 * 
	 *  A method that will get the summation from three numbers
	 * 
	 * @param number1
	 * @param number2
	 * @param number3
	 * @return
	 */
	public int getSum(int number1, int number2, int number3) {
		
		return number1 + number2 + number3;
		
	}
	
	/**
	 * 
	 *  A method that will get the multiplies from three numbers
	 * 
	 * @param number1
	 * @param number2
	 * @param number3
	 * @return
	 */
	public int getMultiplication(int number1, int number2, int number3) {
		
		return number1 * number2 * number3;
		
	}
}
